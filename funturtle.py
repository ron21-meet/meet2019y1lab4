import turtle
ron = turtle.Turtle()
ron.shape("turtle")
finn = turtle.clone()
finn.shape("square")
finn.goto(100,0)
finn.goto(100,100)
finn.goto(0,100)
finn.goto(0,0)
charlie = turtle.clone()
charlie.shape("triangle")
charlie.goto(-100,0)
charlie.goto(-50,100)
charlie.goto(0,0)
finn.goto(200,0)
finn.stamp()
finn.goto(400,0)
ron.goto(-200,0)
ron.stamp()
ron.goto(-400,0)
ron.clearstamps()

turtle.done()
